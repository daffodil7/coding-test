# 양꼬치 문제
# 머쓱이네 양꼬치 가게는 10인분을 먹으면 음료수 하나를 서비스 양꼬치는 1인분에 12,000원, 음료수는 2,000원 
# 정수 n과 k가 매개변수로 주어졌을 때, 양꼬치 n인분과 음료수 k개를 먹었다면 총얼마를 지불해야 하는지 return 하도록 solution 함수를 완성


# 배열 자르기
# 정수 배열 numbers와 정수 num1, num2가 매개변수로 주어질 때, numbers의 num1번 째 인덱스부터 num2번째 인덱스까지 자른 정수 배열을 return 하도록 solution 함수를 완성
def solution(numbers, num1, num2):
    return numbers[num1:num2+1]
